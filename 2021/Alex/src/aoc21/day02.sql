create or alter proc #day02_part1
    @input varchar(max)
as
begin
    create table #directions(dir varchar(20), amount int);
    insert into #directions
    select 
        case
            when charindex('forward', value) > 0 then 'horizontal'
            when charindex('up',      value) > 0 then 'vertical'
            when charindex('down',    value) > 0 then 'vertical'
        end,
        case
            when charindex('forward', value) > 0 then  try_convert(int, replace(trim(char(13) from value), 'forward ', ''))
            when charindex('up',      value) > 0 then -try_convert(int, replace(trim(char(13) from value), 'up ', ''))
            when charindex('down',    value) > 0 then  try_convert(int, replace(trim(char(13) from value), 'down ', ''))
        end
    from string_split(@input collate database_default, char(10))
    where value <> '';
    
    select cast(exp(sum(log(value))) as int)
    from (
        select sum(amount)
        from #directions
        group by dir
    ) tmp(value)
end
go
create or alter proc #day02_part2
    @input varchar(max)
as
begin
    create table #directions(id int identity(1,1), dir varchar(20), horizontal int, aim int);
    insert into #directions(dir, horizontal, aim)
    select
        case
            when charindex('forward', value) > 0 then 'horizontal'
            when charindex('up',      value) > 0 then 'aim'
            when charindex('down',    value) > 0 then 'aim'
        end,
        case
            when charindex('forward', value) > 0 then try_convert(int, replace(trim(char(13) from value), 'forward ', ''))
            when charindex('up',      value) > 0 then 0
            when charindex('down',    value) > 0 then 0
        end,
        case
            when charindex('forward', value) > 0 then  0
            when charindex('up',      value) > 0 then -try_convert(int, replace(trim(char(13) from value), 'up ', ''))
            when charindex('down',    value) > 0 then  try_convert(int, replace(trim(char(13) from value), 'down ', ''))
        end
    from string_split(@input collate database_default, char(10))
    where value <> '';

    select id, sum(aim) over (partition by dir order by id) as accAim
    into #acc
    from #directions
    where dir = 'aim'
    order by id;

    select dirs.horizontal * isnull(acc.accAim, 0) as sum
    into #sums
    from #directions dirs
    cross apply (
        select top 1 accAim
        from #acc
        where id < dirs.id
        order by id desc
    ) acc
    where dir = 'horizontal'
    order by dirs.id

    select (select sum(#sums.sum) from #sums) * (select sum(horizontal) from #directions)
end
go
